    private void AceptarPaginasActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_AceptarPaginasActionPerformed
        // TODO add your handling code here:        
        String line = new String(campo_primerasPaginas.getText());        
        StringTokenizer st = new StringTokenizer(line,"\n");                
        while (st.hasMoreTokens()){
            String pepe = st.nextToken();
            //System.out.println(pepe);
            texto = ""+pepe;
        }           
        //System.out.println(texto);
        if (!line.isEmpty()){
            JOptionPane.showMessageDialog(null,"Se cargaron las primeras paginas de forma correcta");            
        }else{
            JOptionPane.showMessageDialog(null,"No se cargo ninguna informaci√≥n");
        }       
        if (frameDetalle == null){
            framePrimerasP.setEnabled(true);
            framePrimerasP.setVisible(true);
        }
        else
            if (framePrimerasP == null){
                frameDetalle.setEnabled(true);
                frameDetalle.setVisible(true);
            }
        this.setVisible(false);   
        
        
    }//GEN-LAST:event_AceptarPaginasActionPerformed

